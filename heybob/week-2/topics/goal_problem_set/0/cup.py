### 📝 연습 문제 4: 재활용 컵 모으기

# **문제 설명**
# 한 카페에서는 재활용 컵 4개를 가져오면 무료 음료 1잔을 줍니다. 당신은 현재 재활용 컵 50개를 가지고 있으며, 이 컵들을 최대한 활용하여 음료를 받으려 합니다. 재활용 컵을 모두 교환한 후 받을 수 있는 음료의 총 개수를 구하세요. 또한, 음료를 받으며 생기는 컵도 다시 교환할 수 있습니다.

# 단, 남은 컵의 수가 4개 미만이 되면 더 이상 교환할 수 없습니다.

# **문제를 풀 때 다음을 고려하세요:**
# 1. 처음 가지고 있는 컵을 최대한 교환합니다.
# 2. 음료를 받으면서 생기는 새로운 컵도 다시 사용하여 교환할 수 있습니다.
# 3. 가능한 모든 교환 과정을 거쳐 최종적으로 받을 수 있는 음료의 총 개수를 구하는 함수를 작성하세요.

# **예시를 통해 이해하기**
# - 처음 50개의 컵으로 12잔의 음료를 받습니다. (50 // 4 = 12, 남는 컵 2개)
# - 남은 2잔과 받은 12잔, 총 14잔으로 추가로 3잔의 음료를 받습니다. (14 // 4 = 3, 남는 컵 2개)
# - 남은 2잔과 받은 3잔, 총 5잔으로 추가로 1잔을 받으며, 교환 종료.

#   - 총 받는 음료의 수는 12 + 3 + 1 = 16잔입니다.

n = int(input())
total = 0
while n >= 4:
    total += (n // 4)
    n = (n // 4) + (n % 4)

print(total)